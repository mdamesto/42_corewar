int		ft_multi_strchr(char *s, char *src)
{
	char *bufs;

	bufs = s;
	while (*src)
	{
		while (*s)
		{
			if (*s == *src)
				return (1);
			s++;
		}
		s = bufs;
		src++;
	}
	return (0);
}
