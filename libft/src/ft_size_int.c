#include <libft.h>

int		ft_size_int(int n)
{
	int		i;

	i = 1;
	if (n < 0)
	{
		if (n == INT_MIN)
			n++;
		n *= -1;
		i++;
	}
	while (n >= 10)
	{
		n /= 10;
		i++;
	}
	return (i);
}
