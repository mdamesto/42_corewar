#include <libft.h>

void	ft_putnbr_uint(unsigned int n)
{
	if (n >= 10)
	{
		ft_putnbr_uint(n / 10);
		ft_putnbr_uint(n % 10);
	}
	else
		ft_putchar(48 + n);
}
